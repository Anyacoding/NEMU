static def_DHelper(J) {
  sword_t simm = (s->isa.instr.j.imm19_12 << 12) | (s->isa.instr.j.imm11 << 11) | (s->isa.instr.j.imm10_1 << 1);
  //printf("%#x\n", s->isa.instr.val);
  if (s->isa.instr.j.imm20 == 1) { simm |= 0xfff00000; }
  // printf("偏移量为：%#x\n", simm);
  // printf("跳转地址为：%#x\n", simm + (sword_t)s->snpc - 4);
  decode_op_i(s, id_src1, simm + (sword_t)s->snpc - 4, false);
  decode_op_r(s, id_dest, s->isa.instr.j.rd, true);
}

static def_DHelper(R) {
  decode_op_r(s, id_dest, s->isa.instr.r.rd, true);
  decode_op_r(s, id_src1, s->isa.instr.r.rs1, false);
  decode_op_r(s, id_src2, s->isa.instr.r.rs2, false);
}

static def_DHelper(B) {
  sword_t simm = (s->isa.instr.b.imm11 << 11) | (s->isa.instr.b.imm10_5 << 5) | (s->isa.instr.b.imm4_1 << 1);
  //printf("%#x\n", s->isa.instr.val);
  if (s->isa.instr.b.imm12 == 1) { simm |= 0xfffff000; }
  // printf("偏移量为：%#x\n", simm);
  decode_op_i(s, id_dest, simm + (sword_t)s->snpc - 4, true);
  decode_op_r(s, id_src1, s->isa.instr.b.rs1, false);
  decode_op_r(s, id_src2, s->isa.instr.b.rs2, false);
}

